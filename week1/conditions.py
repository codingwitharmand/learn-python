print("Control Flow - if, elif, else")
print("Basic calculator")
a = 10
b = 3

operator = input("Enter operator: ")

if operator == "+":
    print("a + b = ", a + b)
elif operator == "*":
    print("a * b = ", a * b)
elif operator == "-":
    print("a - b = ", a - b)
elif operator == "/":
    print("a / b = ", a / b)
else:
    print("not operator")


